#Solicita ao Usuário para digitar m número
valor = int(input("Digite um número: "))

#Calcula o antecessor e o sucessor
antecessor = valor - 1
sucessor = valor + 1

#Exibe o resiltado do antecessor e sucessor
print(f"O antecessor do {valor} é {antecessor}.")
print(f"O sucessor de {valor} é {sucessor}.")